x=str("Hello Python")  #string func
print(x)
print(type(x))
#---------------------------------------------------------

x=int("187")  # type conversion   string to integer
print(x)
print(type(x))
#---------------------------------------------------------

x=int(False)
print(x)
print(type(x))   #False = 0   (Binary System)
#---------------------------------------------------------

x=int(True)
print(x)
print(type(x))   #True=1  (Binary System)
#---------------------------------------------------------
x=complex(2,3)   # 2 => reel num      3=>imaginare number    | ( ,x) =>0+xj  |  /  | (x, ) =>  x+0j  |   /   |    (x,y) => x + yj
print(x)
print(type(x)) 
#---------------------------------------------------------
x=bool(-81293612739238123981623) 
print(x)
print(type(x)) 
